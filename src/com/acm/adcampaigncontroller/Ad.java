package com.acm.adcampaigncontroller;

public class Ad {
	private String adName;
	private String adType;
	
	public void setAdName(String adName){
		this.adName = adName;
	}
	
	public String getAdName(){
		return adName;
	}
	public void setAdType(String adType){
		this.adType = adType;
	}
	
	public String getAdType(){
		return adType;
	}
	

}
